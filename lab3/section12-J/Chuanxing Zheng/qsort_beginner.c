// Input: A pointer to an array (i.e. the array itself points to the first index)
//        The size of the array (Because we do not know how big the array is automatically)
void printIntArray(int* array, unsigned int size) {
    unsigned int i; // Note: 'unsigned int' is a datatype for storing positive integers.
    for (i = 0; i < size; i++) {
        printf("%d ", array[i]);
    }
    printf("\n");
}

// =============== Helper Functions ===============

// Name: partition
// Input(s):
//  - 'arr' is the array we want to partition.
//  - 'low' is the starting index for the partition.
//  - 'high' is the ending index for the partition.
// Output: This function returns the index of the pivot element placed at its correct position.
int partition(int arr[], int low, int high) {
    // Choose the rightmost element as pivot
    int pivot = arr[high];

    // Pointer for greater element
    int i = (low - 1);

    // Traverse through all elements
    // compare each element with pivot
    for (int j = low; j < high; j++) {
        if (arr[j] <= pivot) {
            // Swap elements at i and j
            i++;
            int temp = arr[i];
            arr[i] = arr[j];
            arr[j] = temp;
        }
    }

    // Swap the pivot element with the greater element specified by i
    i++;
    int temp = arr[i];
    arr[i] = arr[high];
    arr[high] = temp;

    // Return the position from where partition is done
    return i;
}

// Name: quickSort
// Input(s):
//  - 'arr' is the array we want to sort.
//  - 'low' is the starting index for the sort.
//  - 'high' is the ending index for the sort.
// Output: This function doesn't return anything. It sorts the array in-place.
void quickSort(int arr[], int low, int high) {
    if (low < high) {
        // Find pivot element such that
        // elements smaller than pivot are on the left
        // elements greater than pivot are on the right
        int pi = partition(arr, low, high);
        
        // Recursive call on the left of pivot
        quickSort(arr, low, pi - 1);

        // Recursive call on the right of pivot
        quickSort(arr, pi + 1, high);
    }
}

// =============== Sort Function ===============
// Provided below is a sort function. Use the following
//  format for your helper function documentation:
// Name: sortIntegers
// Input(s):
//  - 'array' is a pointer to an integer address.
//     This is the start of some 'contiguous block of memory' that we will sort.
//  - 'size' tells us how big the array of data is we are sorting.
// Output: No value is returned, but 'array' should be modified to store a sorted array of numbers.
void sortIntegers(int* array, unsigned int size){
    quickSort(array, 0, size - 1);    
}

