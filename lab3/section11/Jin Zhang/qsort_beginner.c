// output:recursively call the partion point to devide the array,
// and quick sort left and right sides until the the devided array does not 
// meet the consition. 
void quickSort(int arr[], int low, int high) {
    if (low < high) {
        // get the partition point to devide the array
        int partitionPoint = partition(arr, low, high);

        // left side of array
        quickSort (arr, low, partitionPoint-1);
        // right side
        quickSort (arr, partitionPoint+1, high);
     }
}

