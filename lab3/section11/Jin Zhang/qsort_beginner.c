// Partition function.
int partition(int arr[], int low, int high) {
    // assign the pivot to the end of the array
    int pivot = arr[high];

    // initate i to low boundary. 
    // means that no elements have been sorted yet
    // it works like a dividing line on the left-most array
    int i = low-1;

    // for loop starts to iterate every element from the array
    // until the last number before the pivot
    for (int j = low; j < high; j++) {
        
        //if the element is smaller than the pivot
        if (arr[j] <= pivot) {
            // add the boundary line(or it means one more element smaller than pivot)  
            i++;
            // exchange this small element with the element on the right on the boundary 
            int temp = arr[i];
            arr[i] = arr[j];
            arr[j] = temp;
        }
    }

    // finish the for-loop. Current i+1 is the number of
    // elelments smaller than pivot(c is 0-indexed).
    // exchange the pivot with the element on the right of index of i
    int temp_part = arr[i+1];
    arr[i+1] = arr[high];
    arr[high] = temp_part;

    // the current partition position also the element that
    // has been sorted. 
    return i+1;
}

